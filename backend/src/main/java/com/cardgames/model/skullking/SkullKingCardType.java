package com.cardgames.model.skullking;

/**
 * Enumeration of the different types of cards in Skull King.
 */
public enum SkullKingCardType {
    /**
     * Standard number card (1-14).
     */
    NUMBER,

    /**
     * Pirate special card.
     */
    PIRATE,

    /**
     * Mermaid special card.
     */
    MERMAID,

    /**
     * Skull King special card.
     */
    SKULL_KING,

    /**
     * Escape special card (white flag).
     */
    ESCAPE
}
