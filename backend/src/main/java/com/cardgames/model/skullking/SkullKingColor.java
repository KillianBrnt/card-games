package com.cardgames.model.skullking;

/**
 * Enumeration of the different card colors (suits) in Skull King.
 */
public enum SkullKingColor {
    /**
     * Yellow suit.
     */
    YELLOW,

    /**
     * Purple suit.
     */
    PURPLE,

    /**
     * Green suit.
     */
    GREEN,

    /**
     * Red suit.
     */
    RED,

    /**
     * Black suit (Trump).
     */
    BLACK,

    /**
     * No specific color (for special cards).
     */
    NONE
}
