package com.cardgames.model.uno;

/**
 * Enumeration of the different colors of Uno cards.
 */
public enum UnoCardColor {
    /**
     * Red color.
     */
    RED,

    /**
     * Blue color.
     */
    BLUE,

    /**
     * Green color.
     */
    GREEN,

    /**
     * Yellow color.
     */
    YELLOW,

    /**
     * No specific color (e.g., for Wild cards before a color is chosen).
     */
    NONE
}
